module binary_to_bcd (

    input  logic [4:0] In,
    output logic [7:0] y

);

always_comb begin 
    case (In)
        
        5'b00000 : y = 8'b00000000;
        5'b00001 : y = 8'b00000001;
        5'b00010 : y = 8'b00000010;
        5'b00011 : y = 8'b00000011;
        5'b00100 : y = 8'b00000100;
        5'b00101 : y = 8'b00000101;
        5'b00110 : y = 8'b00000110;
        5'b00111 : y = 8'b00000111;
        5'b01000 : y = 8'b00001000;
        5'b01001 : y = 8'b00001001;

        5'b01010 : y = 8'b00010000; // 10
        5'b01011 : y = 8'b00010001; // 11
        5'b01100 : y = 8'b00010010; // 12
        5'b01101 : y = 8'b00010011; // 13
        5'b01110 : y = 8'b00010100; // 14
        5'b01111 : y = 8'b00010101; // 15
        5'b10000 : y = 8'b00010110; // 16
        5'b10001 : y = 8'b00010111; // 17
        5'b10010 : y = 8'b00011000; // 18
        5'b10011 : y = 8'b00011001; // 19

        5'b10100 : y = 8'b00100000; // 20
        5'b10101 : y = 8'b00100001; // 21
        5'b10110 : y = 8'b00100010; // 22
        5'b10111 : y = 8'b00100011; // 23
        5'b11000 : y = 8'b00100100; // 24
        5'b11001 : y = 8'b00100101; // 25
        5'b11010 : y = 8'b00100110; // 26
        5'b11011 : y = 8'b00100111; // 27
        5'b11100 : y = 8'b00101000; // 28
        5'b11101 : y = 8'b00101001; // 29
        5'b11110 : y = 8'b00110000; // 30
        5'b11111 : y = 8'b00110001; // 31

        default: y = 8'b00000000;

    endcase
end

endmodule





